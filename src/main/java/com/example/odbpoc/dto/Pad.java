package com.example.odbpoc.dto;

public class Pad {
    Long id;
    Long x;
    Long y;
    Long symbolIndex;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getX() {
        return x;
    }

    public void setX(Long x) {
        this.x = x;
    }

    public Long getY() {
        return y;
    }

    public void setY(Long y) {
        this.y = y;
    }

    public Long getSymbolIndex() {
        return symbolIndex;
    }

    public void setSymbolIndex(Long symbolIndex) {
        this.symbolIndex = symbolIndex;
    }
}
